# SOAL 2 - Menulis kelipatan bilangan Bulat
# Tuliskan program untuk Soal 2 di bawah ini
num = int(input("Masukan angka bilangan bulat: "))
a = num * 1
print ("Kelipatan 1 adalah :", a)
b = num * 2
print ("Kelipatan 2 adalah :", b)
c = num * 3
print ("Kelipatan 3 adalah :", c)
d = num * 4
print ("Kelipatan 4 adalah :", d)
e = num * 5
print ("Kelipatan 5 adalah :", e)

print ("kelipatan dari angka",num ,"adalah: ", a, b, c, d, e)
print()

